let dia = prompt("¿Cuál es el dia de la semana?");

switch (dia) {
    case "lunes":
        document.write("Puto lunes de mierda");
        break;
        
    case "martes":
    case "miercoles":
    case "jueves":
        document.write("Otro dia mas en la oficina");
        break;
    case "viernes":
        document.write("Hoy no es jueves, hoy es vierneeeeeeesss!!!! MAMAGUEVO");
        break;
    case "sabado":
        document.write("Vamooooooos esta noche me chispo");
        break;
    case "domingo":
        document.write("Me cago en to lo cagable mañana lunes que puto asco");
        break;
    default:
        document.write("Eres tonto?, no te sabes los dia de la semana, vuelvete a primaria!!");    
        break;
}
