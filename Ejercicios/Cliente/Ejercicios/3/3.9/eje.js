let num=Number(prompt("Introduce un numero 0 de 10"));

switch (num) {
    case 0:
        document.write("cero");
        break;
    case 1:
        document.write("uno");
        break;
    case 2:
        document.write("dos");
        break;
    case 3:
        document.write("tres");
        break;
    case 4:
        document.write("cuatro");
        break;
    case 5:
        document.write("cinco");
        break;
    case 6:
        document.write("seis");
        break;
    case 7:
        document.write("siete");
        break;
    case 8:
        document.write("ocho");
        break;
    case 9:
        document.write("nueve");
        break;
    case 10:
        document.write("diez");
        break;
}